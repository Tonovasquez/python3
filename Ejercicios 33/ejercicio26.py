#Elabore un programa que lea 3 numeros enteros positivos y que muestre la suma,
#la resta y la multiplicacion de todos. El resultado debe ser siempre positivo
n1 = int(input("ingrese numero:."))
n2 = int(input("ingrese numero:."))
n3 = int(input("ingrese numero:."))
suma = n1 + n2 + n3
print("la suma es de:.{}".format(suma))
resta = n1 - n2 - n3
print("la resta es de:.{}".format(resta))
multiplicacion = n1 * n2 * n3
print("la multiplicacion es de:.{}".format(multiplicacion))
